#include <unistd.h>

void	ft_print_comb(void)
{
	char	tab[3];

	tab[0] = '0';
	while (tab[0] <= '7')
	{
		tab[1] = tab[0] + 1;
		while (tab[1] <= '8')
		{
			tab[2] = tab[1] + 1;
			while (tab[2] <= '9')
			{
				write(1, tab, 3);
				if (tab[0] != '7')
				{
					write(1, ", ", 2);
				}
				tab[2]++;
			}
		tab[1]++;
		}
	tab[0]++;
	}
}

